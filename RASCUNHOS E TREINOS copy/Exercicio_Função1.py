def imprimir_Antecessor_e_Sucessor(n):
    Antecessor = n - 1
    Sucessor = n + 1
    print(f'Antecessor: {Antecessor} , Sucessor: {Sucessor}')
    
n = int(input("Digite um numero n: "))

imprimir_Antecessor_e_Sucessor(n)

def suce_ante(n):
    sucessor = n + 1
    antecessor = n - 1
    print(f'Sucessor: {sucessor} Antecessor: {antecessor}')

n = int(input('Digite um n: '))

suce_ante(n)



